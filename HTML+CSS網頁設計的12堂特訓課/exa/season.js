month=6;  
if (2<=month && month<=4)
    console.log('充滿生機的春天');    
else if (5<=month && month<=7)
    console.log('熱力四射的夏季');
else if (month>=8 && month <=10)
    console.log('落葉繽紛的秋季');
else if (month==1 || (month>=11 && month<=12))
    console.log('寒風刺骨的冬季');
else
    console.log('很抱歉沒有這個月份!!!');